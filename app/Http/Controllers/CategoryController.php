     */
    public function show(Category $category)
    {
        echo "<h1>Category</h1>";
        echo "<p>#{$category->id} {$category->title}</p>";

        $posts = $category->posts()->get();

        if($posts){
            echo "<h1>Category</h1>";
            foreach($posts as $post):
                echo "<p>#{$post->id} {$post->title}</p>";
            endforeach;
        }

    }

